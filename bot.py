# /start — ГЛАВНОЕ МЕНЮ
# ============================================

@dp.message(Command('start'))
async def cmd_start(message: types.Message):
    user_id = message.from_user.id
    username = message.from_user.username or message.from_user.first_name
    
    logger.info(f"Пользователь {username} ({user_id}) открыл бота")
    
    # Создаём меню со статьями
    builder = InlineKeyboardBuilder()
    
    for article_id, article_data in ARTICLES.items():
        builder.button(
            text=f"{article_data['title']}",
            callback_data=f"article_{article_id}"
        )
    
    builder.adjust(1)
    
    # БЕЗ parse_mode — избегаем ошибок с username
    await message.answer(
        f"👋 Привет, {username}!\n\n"
        " ВЫБЕРИТЕ СТАТЬЮ:\n\n"
        "Нажмите на кнопку ниже, чтобы получить продолжение:",
        reply_markup=builder.as_markup()
    )

