async def send_long_message(target, text: str, reply_markup=None):
    """
    Отправляет длинное сообщение, разбивая его на части.
    target может быть Message или CallbackQuery.message
    """
    # Если текст короче лимита — отправляем целиком
    if len(text) <= MAX_MESSAGE_LENGTH:
        if reply_markup:
            await target.answer(text, reply_markup=reply_markup)
        else:
            await target.answer(text)
        return
    
    # Разбиваем текст на части
    parts = split_text_into_parts(text, MAX_MESSAGE_LENGTH)
    
    logger.info(f"📄 Длинное сообщение разбито на {len(parts)} частей")
    
    # Отправляем каждую часть
    for i, part in enumerate(parts):
        # Добавляем номер части (кроме последней)
        if len(parts) > 1:
            header = f"📖 Часть {i+1}/{len(parts)}\n\n"
            part_with_header = header + part
        else:
            part_with_header = part
        
        # На последнюю часть добавляем клавиатуру (если есть)
        if i == len(parts) - 1 and reply_markup:
            await target.answer(part_with_header, reply_markup=reply_markup)
        else:
            await target.answer(part_with_header)
        
        # Задержка между частями (чтобы не было flood)
        if i < len(parts) - 1:
            await asyncio.sleep(0.5)

def split_text_into_parts(text: str, max_length: int):
    """
    Разбивает текст на части по абзацам, не превышая max_length.
    """
    parts = []
    current_part = ""
    
    # Разбиваем по двойным переносам строк (абзацы)
    paragraphs = text.split('\n\n')
    
    for paragraph in paragraphs:
        # Если абзац сам по себе длиннее лимита — разбиваем его по строкам
        if len(paragraph) > max_length:
            # Сначала сохраняем текущую часть
            if current_part:
                parts.append(current_part.strip())
                current_part = ""
            
            # Разбиваем длинный абзац по строкам
            lines = paragraph.split('\n')
            for line in lines:
                if len(current_part) + len(line) + 2 > max_length:
                    if current_part:
                        parts.append(current_part.strip())
                    current_part = line + '\n'
                else:
                    current_part += line + '\n'
        # Если абзац помещается — добавляем к текущей части
        elif len(current_part) + len(paragraph) + 2 > max_length:
            parts.append(current_part.strip())
            current_part = paragraph + '\n\n'
        else:
            current_part += paragraph + '\n\n'
    
    # Добавляем последнюю часть
    if current_part:
        parts.append(current_part.strip())
    
    return parts

