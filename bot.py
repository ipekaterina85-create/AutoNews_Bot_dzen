@dp.callback_query(lambda c: c.data.startswith('article_'))
async def select_article(callback_query: types.CallbackQuery):
    user_id = callback_query.from_user.id
    username = callback_query.from_user.username or callback_query.from_user.first_name
    
    # Извлекаем ID статьи
    article_id = callback_query.data.replace('article_', '')
    
    if article_id not in ARTICLES:
        await callback_query.answer("❌ Статья не найдена", show_alert=True)
        return
    
    article = ARTICLES[article_id]
    
    logger.info(f"Пользователь {username} выбрал статью: {article_id}")
    
    # Проверяем подписку
    is_subscribed = await check_subscription(user_id)
    
    if is_subscribed:
        # Отправляем продолжение
        await callback_query.message.answer(
            f"✅ {article['title']}\n\n"
            f"📖 {article['description']}\n"
            "Отправляю продолжение..."
        )
        await callback_query.message.answer(article['text'])
